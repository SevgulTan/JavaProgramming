package day03_EscapeSequences;

public class EscapeSequences {
 /*
 Escape Sequences: Must given with double quote
 \n : starts a new line
  */
 public static void main(String[] args) {
     System.out.println("Java \n Pyton \n C#");

     System.out.println("\t Java is good programming language.");
     System.out.println("----------------------------------------------------");
     System.out.println("/ \\"); // in order to print one backslash.
     System.out.println("----------------------------------------------------");
     System.out.println("My favourite Tv show is \"Game of Thrones\"");
 }
}
