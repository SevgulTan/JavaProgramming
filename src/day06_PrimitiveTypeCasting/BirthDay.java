package day06_PrimitiveTypeCasting;

public class BirthDay {

    public static void main(String[] args) {
        String name ="John";
        int birtDay =25;
        String month ="April";
        int birthYear = 1995;

        // name was born on month/day/year.
        System.out.println(name+ " was born on "+ month+"/"+birtDay+"/"+birthYear);

        // My favourite book is "bookname"

        String bookName ="Alamut";
        System.out.println("My favourite book is "+"\""+bookName+"\"");




    }
}
